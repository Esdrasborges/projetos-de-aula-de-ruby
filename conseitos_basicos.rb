Tipos de dados : É um conceito que existe para classisficar os dados

Tipos de Dados: 
INTEGER:  Como na matemática , inteiro é  o tipo de dado que representa o conjunto de números positivos, negativos e 0
inteiro = 10
FLOAT: Tipo qu representa os numeros reais inexatos. De forma abreviada, são numeros decimais, numeros que contêm ponto flutuante.
float = 10.01
BOOLEAN : Tipo de dado usado para informar a veracidade de algo. Possue apenas dois estados, sendo ele True (Verdadeiro) que é umai instância da classe TrueClasse e False (Falso) que é uma instância da classe FalseClass.
boolean = True ou false
STRING : Tipo que representa um texto. Um conjunto de letras, símbolos ou números (ou caracteres). Pode ser definitivo de varias formas, porém, as mas comuns são dentro de aspas simples ou duplas.
string = "Ola mundo"
ARRAY : Um tipo que perminte armazenar uma lista ordenada de dados em um único objeto, para definr um array você deve ultilizar colchetes.
bitcode_array = ['position_0', 'position_1', 'position_2']
pode acessar qualque objetos no array apenas iformando a possição. O primeiro valor a ser pegado pelo array tem que ser o 0
SYMBOL : símbolos é um tipo de dados semelhantes a STRING, com a diferenã de que ele é imutavel. Duas STRING idênticas podem ser objetos diferentes, mas um símbolo é apenas um objeto, ocupando sempre o mesmo espaço na memoria. Um simbolo sempre é definido começando com dois pontos : seguido de seu nome.
HASH : Tipo que representa uma coleção de dados organizados por chaves e seus respectivos valores. Enquanto ARRAY são definidos com colchetes, hashes são definidos com chaves '{}'
onebit_hash = {couser: 'ruby', language: 'pt-br', locale: 'onebitcode.com'}
couser, language e locale : São considerado chaves, sempre os dois pontos devem suceder a eles.
ruby, pt-br e onebitcode.com : São considerado valores, sempre entres aspas.
são usados as chaves para fazer a chamada dentro do codigo 
    onibit_hash[:course]
    "ruby"
diferente do array que chama pela posição usando numeros em ordem    

    

