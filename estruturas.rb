Estrutura de Controle 
Trata-se de códigos que escrevemos em nossos programas para analisar dados e decidir qual caminho seguir. Divide-se em dois tipos: Condicional e Interação
CONDICIONAL: É um tipo de estrturura de controle que execulta um código dependendo do resultado de uma condição 
    if, else, elsif, unless e case 
IF: (e se) verifica se uma condição é verdadeira (true), se for ele execulta o conjunto de códigos que está dentro da estrturura.
ELSE: (se nao) usado de forma associado ao if caso o comando não o execulte (ou se if for false) ele procura o else.
ELSIF: (mas se) ultilizado quando a nescessidade de verificar mais de uma condição em um if.   
UNLESS: (a nao ser que) ao contario de if, unless é execultado quando a condição fo falsa.
CASE: (caso) varias condições aplicadas a ele ou uma associção de if, elsif 

INTERAÇÃO: Tipo de estrutura de controle que gerencia quantas vezes um trecho de código será exexutado.
FOR, WHILE, TIMES, DO/WHILE
FOR: (para) usado para percorrer uma coleção de elementos
TIMES: (tempo) execulta uma repetição de codigos por um determinado numero de vezes.
WHILE: (enquanto) Instrução que repete um bloco de codigos enquanto sua condição for verdadeira.
DO/WHILE - LOOP: (fazer enquanto - ciclo) Ele cria uma laço que é só paradp quando uma instrução BREAK for verdadeira.